package com.faure.beans;

public class Utilisateur {
	
	private String nom;
	private String prenom;
	private String sexe;
	private Boolean connaitJ2EE;
	
	public String getNom() {
		return nom;
	}
	
	public void setNom(String nom) {
		this.nom = nom;
	}
	
	public String getPrenom() {
		return prenom;
	}
	
	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}
	
	public String getSexe() {
		return sexe;
	}
	
	public void setSexe(String sexe) {
		this.sexe = sexe;
	}
	
	public Boolean getConnaitJ2EE() {
		return connaitJ2EE;
	}
	
	public void setConnaitJ2EE(Boolean connaitJ2EE) {
		this.connaitJ2EE = connaitJ2EE;
	}
	
	

}
